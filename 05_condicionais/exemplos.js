function exemploIf() {
    var maca = "banana"
    if (maca == "banana") {
        console.log("Banana")
    }
}

function exemploIfElse() {
    var maca = "banana"
    if (maca == "maçã") {
        console.log("maçã")
    } else {
        console.log("Não é maçã")
    }
}

function exemploSwitch() {
    var opcao = 1;
    switch(opcao){
        case 1:
            console.log("Opção 1");
        case 2:
            console.log("Opção 2");
    }
}